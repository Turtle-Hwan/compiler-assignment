// Test 01: Basic Arithmetic Operations
// Purpose: Test all arithmetic operators (+, -, *, /, %)
// Expected: 15, 5, 50, 2, 0

function main() {
    let a = 10;
    let b = 5;

    // Addition
    console.log(a + b);   // 15

    // Subtraction
    console.log(a - b);   // 5

    // Multiplication
    console.log(a * b);   // 50

    // Division
    console.log(a / b);   // 2

    // Modulo
    console.log(a % b);   // 0

    return 0;
}
